package fr.edwinalkins.ihm.util;

public final class MarginDouble {

	public final double left;
	public final double right;
	public final double up;
	public final double bottom;
	
	public MarginDouble(double l, double r, double u, double b) {
		this.left = l;
		this.right = r;
		this.up = u;
		this.bottom = b;
	}
}
